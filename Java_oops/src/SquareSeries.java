
public class SquareSeries {
       
	    public static void main(String[] args) {
	        
	        for (int i = 0; i <= 35; i++) {
	            System.out.print(i * i);
	            
	            // Print comma except after the last number
	            if (i < 35) {
	                System.out.print(", ");
	            }
	        }
	    }
	}


